# BASED ON FRUTAI (FRIEND RECOMMENDATION SYSTEM USING A USER'S TOTAL ATTRIBUTES INFORMATION)

#import random and numpy for random number generation for candidates
import random
import numpy as np

class user: # FOR CREATING A USER
    def __init__(self, user_num, user_gender, user_location, friends=None):
        self.user_num = user_num
        self.user_gender = user_gender
        self.user_location = user_location
        
        self.friends = friends if friends is not None else []
        self.candidates = []
        self.potential = 0 #used when the user is a different user's candidate

    def get_friends(self):
        return self.friends

    def get_candidates(self):
        return self.candidates

    def add_candidate(self, new_candidate):
        self.candidates.append(new_candidate)

    def add_friend(self, new_friends): #new_friends is a list of friends to add
        if isinstance(new_friends, list): #double check that it's a list
            for friend in new_friends:
                #make sure each friend is a user and the friend is not already a friend
                if isinstance(friend, user) and friend not in self.friends:
                    self.friends.append(friend)

class social_media: # CALCULATING FRIEND RECOMMENDATIONS HERE
    def __init__(self):
        self.users = []

    def add_user(self, new_user):
        self.users.append(new_user)

    def get_cor(self, user): # RETURNS CORRELATION VALUE FOR GENDER AND LOCATION
        g_cor = 0
        loc_cor = 0

        # USE JACCARDS COEFFICIENT TO CALC CORRELATION

        i_g = 0 # intersection for gender
        u_g = 0 # union for gender
        i_loc = 0 # intersection for location
        u_loc = 0 # union for location

        for friend in user.get_friends():
            if friend.user_gender == user.user_gender:
                i_g += 1
            u_g += 1

            if friend.user_location == user.user_location:
                i_loc += 1
            u_loc += 1

        if u_g != 0: # in order to not divide by 0
            g_cor = i_g / u_g
        else:
            g_cor = 0

        if u_loc != 0: # in order to not divide by 0
            loc_cor = i_loc / u_loc
        else:
            loc_cor = 0
        
        return g_cor, loc_cor

    def assign_candidates(self, cand_count=10): #randomly assigns candidates for all users in the social_media
        users = [x.user_num for x in self.users]
        for y in self.users:
            candidates1 = [cand_num for cand_num in users if cand_num != y.user_num] #list of candidate numbers (not including user)
            candidates_final = random.sample(candidates1, cand_count) #randomly choose 10 candidates from the candidates list

            for cand_num in candidates_final:
                candidate = next(z for z in self.users if z.user_num == cand_num) #go through list of candidates
                y.add_candidate(candidate) #add candidates for that user

    def recommend_friends(self, user):
        # take friends list of user and using correlation
        # of friends, apply correlation on the candidates
        # to rank the candidates

        g_cor, loc_cor = self.get_cor(user)
        
        all_friends = user.get_friends()
        all_candidates = user.get_candidates()

        for candidate in all_candidates:
            candidate.potential = 0 # reset in case the program has already been run before
            
            if candidate.user_location == user.user_location:
                candidate.potential += loc_cor # if loc matches, add cor to total potential
            if candidate.user_gender == user.user_gender:
                candidate.potential += g_cor # if g matches, add cor to total potential

        # for every friend in common, add 1 to potential
        # (what the original creators of FRUTAI used)
        for cand_friend in candidate.friends:
            for user_friend in user.friends:
                if cand_friend.user_num == user_friend.user_num:
                    candidate.potential += 1

        # for loop here so if one of the candidates is already the user's friend
        # their potential immediately becomes 999 (a very large number)
        for candidate in all_candidates:
            for friend in all_friends:
                if friend.user_num == candidate.user_num:
                    candidate.potential = 999

        # use the candidate potential list to sort the all_candidates list from highest to lowest
        
        # all_candidates is what needs to be sorted, key is so potential is used for sorting
        # and reverse = True means that it sorts with largest potential first and smallest potential last
        sorted_candidates = sorted(all_candidates, key=lambda x: x.potential, reverse=True)

        # print the user and then their candidates in order
        print(user.user_num, [candidate.user_num for candidate in sorted_candidates])

